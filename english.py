def analyze_text_spacy(text):
    """
    Sử dụng spaCy để phân tích từng token trong văn bản và trả về chuỗi kết quả.
    """
    doc = nlp(text)
    analysis_results = []
    for token in doc:
        # Bỏ qua các token chỉ là khoảng trắng
        if token.is_space:
            continue
            
        result = (
            f"Word: {token.text}\n"
            f"  - Lemma (Dạng nguyên thể): {token.lemma_}\n"
            f"  - POS (Loại từ): {token.pos_} ({spacy.explain(token.pos_)})\n"
            f"  - Tag (Thẻ chi tiết): {token.tag_} ({spacy.explain(token.tag_)})\n"
            f"  - Dependency (Phụ thuộc): {token.dep_}\n"
            f"  - Is Stopword (Từ dừng): {token.is_stop}\n"
        )
        analysis_results.append(result)
    
    return "--------------------\n".join(analysis_results)

