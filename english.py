# HÀM ĐƠN GIẢN HÓA VĂN BẢN SỬ DỤNG SPACY
# =========================================================================
def simplify_text_spacy(text):
    """
    Sử dụng spaCy để phân tích và đơn giản hóa văn bản dựa trên từ điển lemma.
    """
    doc = nlp(text)
    simplified_parts = []

    for token in doc:
        lemma = token.lemma_.lower()
        if lemma in COMPLEX_TO_SIMPLE_LEMMAS:
            simple_word = COMPLEX_TO_SIMPLE_LEMMAS[lemma]
            if token.is_title:
                output_word = simple_word.capitalize()
            else:
                output_word = simple_word
            simplified_parts.append(output_word + token.whitespace_)
        else:
            simplified_parts.append(token.text_with_ws)

    final_text = "".join(simplified_parts).strip()
    return final_text.capitalize() if final_text else ""

